let num = 9.0998

console.log(Math.floor(num)) // arredonda para baixo
console.log(Math.ceil(num)) // arredonda para cima
console.log(Math.round(num)) // arredonda para o mais proximo
console.log(Math.max(1, 2, 3, 4, 5, 6, 7)) //pega o maior da sequencia
console.log(Math.min(1, 2, 3, 4, 5, 6, 7)) //pega o menor da sequencia
console.log(Math.random()) //gera números aleatorios entre 0 e 1
console.log(25 ** 0.5) //faz raiz quadrada
console.log(100 / 0) //dá infinity, pois o js dá um número tão grade que seu resultado é infinity
